// change_config.go adds PR change types that edit whole config/app files
// (platform/policies/catalogs/access, storage class, per-app storage) and a
// live PR-status refresh. The UI posts the full new file content (it shows the
// current file, the user edits, we open a PR). Durable change = PR only.

// createFileEditPR validates the path and opens a PR replacing one file.
func createFileEditPR(r *http.Request, typ, path, content, reason string) (changeRecord, error) {
	if !relPathOK(path) {
		return changeRecord{}, fmt.Errorf("path not allowed: %s", path)
	}
	if strings.TrimSpace(content) == "" {
		return changeRecord{}, fmt.Errorf("empty content")
	}
	if strings.HasSuffix(path, ".json") {
		var probe any
		if json.Unmarshal([]byte(content), &probe) != nil {
			return changeRecord{}, fmt.Errorf("invalid JSON for %s", path)
		}
	}
	files := []generatedFile{{Path: path, Content: content}}
	title := typ + ": edit " + path
	body := "Generated by homelab control-api.\n\nReason: " + reason + "\n"
	branch := branchName(strings.ReplaceAll(typ, ".", "-"), "edit")
	return createPRChange(r, typ, title, body, branch, files)
}

func fileEditHandler(typ, path, minRole string) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodPost {
			http.Error(w, "POST only", http.StatusMethodNotAllowed)
			return
		}
		if !requireRole(w, r, minRole) {
			return
		}
		var req struct {
			Content string `json:"content"`
			Reason  string `json:"reason"`
		}
		if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
			writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "bad json"})
			return
		}
		rec, err := createFileEditPR(r, typ, path, req.Content, req.Reason)
		if err != nil {
			writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": err.Error(), "change_id": rec.ID})
			return
		}
		writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "branch": rec.Branch, "pr": prResult{Number: rec.PRNumber, URL: rec.PRURL}})
	}
}

