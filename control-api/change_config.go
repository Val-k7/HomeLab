// --- structured Nix generation (catalog add, storage class) ----------------
//
// These used to be whole-file replaces where the WEB DIALOG interpolated user
// input into Nix (insufficient escaping = Nix injection). The server is the
// security boundary, so it now builds the entry itself: atoms go through
// strict allowlist regexes, free text through nixString (escapes \ " $), and
// the entry is spliced into the existing block by index (never regexp
// replacement, so escaped '$'/'\' can't act as metacharacters).

// reCatalogID lives in library.go (shared with /v1/library).
var reCatalogRef = regexp.MustCompile(`^[A-Za-z0-9._/-]{1,100}$`)
var reStorageClassName = regexp.MustCompile(`^[a-zA-Z_][a-zA-Z0-9_-]{0,39}$`)
var reCatalogsBlock = regexp.MustCompile(`(?s)(catalogs = \[\n)(.*?)(\n[ \t]*\];)`)
var reStorageClassesBlock = regexp.MustCompile(`(?s)(storageClasses = \{\n)(.*?)(\n[ \t]*\};)`)

// nixSafeText reports whether free text may be embedded (via nixString) on a
// single line of generated Nix: bounded length, no control characters.
func nixSafeText(s string, max int) bool {
	if len(s) > max {
		return false
	}
	for _, r := range s {
		if r < 0x20 || r == 0x7f {
			return false
		}
	}
	return true
}

// spliceIntoNixBlock inserts entry as the last element of the list/attrset
// matched by block (open, body, close groups). Index splice, not ReplaceAll.
func spliceIntoNixBlock(content string, block *regexp.Regexp, entry string) (string, bool) {
	m := block.FindStringSubmatchIndex(content)
	if m == nil {
		return "", false
	}
	end := m[5] // end of the body group
	return content[:end] + "\n" + entry + content[end:], true
}

type catalogAddRequest struct {
	ID          string `json:"id"`
	Repo        string `json:"repo"`
	Ref         string `json:"ref"`
	Trust       string `json:"trust"`
	Policy      string `json:"policy"`
	Name        string `json:"name"`
	Description string `json:"description"`
	Category    string `json:"category"`
	Reason      string `json:"reason"`
	Content     string `json:"content"` // legacy raw whole-file replace (Settings editor)
}

// buildCatalogEntry generates one config/catalogs.nix list entry from
// validated fields. Pure (unit-tested against injection payloads).
func buildCatalogEntry(req catalogAddRequest) (string, error) {
	if !reCatalogID.MatchString(req.ID) {
		return "", fmt.Errorf("bad catalog id (want [a-z0-9-], alnum start)")
	}
	if !strings.HasPrefix(req.Repo, "https://") || !validNixAtom(req.Repo) || strings.ContainsAny(req.Repo, " \t'") {
		return "", fmt.Errorf("repo must be a plain https:// URL")
	}
	if !reCatalogRef.MatchString(req.Ref) {
		return "", fmt.Errorf("bad ref (tag or commit SHA)")
	}
	switch req.Trust {
	case "official", "community", "untrusted":
	default:
		return "", fmt.Errorf("trust must be official|community|untrusted")
	}
	switch req.Policy {
	case "strict", "warn":
	default:
		return "", fmt.Errorf("policy must be strict|warn")
	}
	switch req.Category {
	case "", "media", "network", "dev", "data", "monitoring", "misc":
	default:
		return "", fmt.Errorf("bad category")
	}
	if !nixSafeText(req.Name, 80) || !nixSafeText(req.Description, 200) {
		return "", fmt.Errorf("name/description too long or has control characters")
	}
	lines := []string{
		"      id = " + nixString(req.ID) + ";",
		"      repo = " + nixString(req.Repo) + ";",
		"      ref = " + nixString(req.Ref) + ";",
		"      trust = " + nixString(req.Trust) + ";",
		"      policy = " + nixString(req.Policy) + ";",
	}
	if req.Name != "" {
		lines = append(lines, "      name = "+nixString(req.Name)+";")
	}
	if req.Description != "" {
		lines = append(lines, "      description = "+nixString(req.Description)+";")
	}
	if req.Category != "" {
		lines = append(lines, "      category = "+nixString(req.Category)+";")
	}
	return "    {\n" + strings.Join(lines, "\n") + "\n    }", nil
}

// catalogAddChangeHandler is now STRUCTURED: the dialog posts fields, the
// server generates and splices the Nix. The legacy whole-file content mode is
// kept for the Settings raw editor (admin — same trust as platform-config).
func catalogAddChangeHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "POST only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "admin") {
		return
	}
	var req catalogAddRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "bad json"})
		return
	}
	if req.ID == "" && req.Content != "" {
		rec, err := createFileEditPR(r, "catalog.config", "config/catalogs.nix", req.Content, req.Reason)
		if err != nil {
			writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": err.Error(), "change_id": rec.ID})
			return
		}
		writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "branch": rec.Branch, "pr": prResult{Number: rec.PRNumber, URL: rec.PRURL}})
		return
	}
	entry, err := buildCatalogEntry(req)
	if err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": err.Error()})
		return
	}
	b, err := os.ReadFile(filepath.Join(sourceDir(), "config", "catalogs.nix"))
	if err != nil {
		writeJSON(w, http.StatusInternalServerError, map[string]any{"ok": false, "error": "cannot read config/catalogs.nix"})
		return
	}
	content := string(b)
	m := reCatalogsBlock.FindStringSubmatch(content)
	if m == nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "catalogs block not found (non-standard file — edit config/catalogs.nix directly)"})
		return
	}
	if regexp.MustCompile(`(?m)^\s*id\s*=\s*"` + regexp.QuoteMeta(req.ID) + `"`).MatchString(m[2]) {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "catalog already exists: " + req.ID})
		return
	}
	next, _ := spliceIntoNixBlock(content, reCatalogsBlock, entry)
	reason := req.Reason
	if reason == "" {
		reason = "add catalog " + req.ID
	}
	rec, err := createFileEditPR(r, "catalog.config", "config/catalogs.nix", next, reason)
	if err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": err.Error(), "change_id": rec.ID})
		return
	}
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "branch": rec.Branch, "pr": prResult{Number: rec.PRNumber, URL: rec.PRURL}})
}

var reCatalogEntryBlock = regexp.MustCompile(`(?s)    \{\n.*?\n    \}`)

// replaceCatalogEntry replaces (or removes, when remove=true) the catalogs.nix
// list entry whose id matches. Returns the new file content, or an HTTP status
// + error when the block or the entry cannot be located. Pure (unit-tested).
func replaceCatalogEntry(content, id, entry string, remove bool) (string, int, error) {
	m := reCatalogsBlock.FindStringSubmatchIndex(content)
	if m == nil {
		return "", http.StatusBadRequest, fmt.Errorf("catalogs block not found (non-standard file — edit config/catalogs.nix directly)")
	}
	body := content[m[4]:m[5]]
	blockStart, blockEnd := -1, -1
	for _, loc := range reCatalogEntryBlock.FindAllStringIndex(body, -1) {
		if strings.Contains(body[loc[0]:loc[1]], `id = "`+id+`"`) {
			blockStart, blockEnd = loc[0], loc[1]
			break
		}
	}
	if blockStart < 0 {
		return "", http.StatusNotFound, fmt.Errorf("catalog not found: %s", id)
	}
	var newBody string
	if remove {
		// Drop the separator newline the deleted entry leaves behind.
		newBody = strings.Trim(body[:blockStart]+body[blockEnd:], "\n")
	} else {
		newBody = body[:blockStart] + entry + body[blockEnd:]
	}
	return content[:m[4]] + newBody + content[m[5]:], http.StatusOK, nil
}

// mutateCatalogEntry updates or removes the catalogs.nix list entry whose id
// matches req.ID, then opens a PR with the new file. Shared by
// /v1/changes/catalog-update and /v1/changes/catalog-remove.
