// buildStorageClassEntry generates one storageClasses attrset line from
// validated fields. Pure (unit-tested against injection payloads).
func buildStorageClassEntry(req storageClassRequest) (string, error) {
	if !reStorageClassName.MatchString(req.Name) {
		return "", fmt.Errorf("bad class name (letter/underscore start, then [a-zA-Z0-9_-], max 40)")
	}
	switch req.Type {
	case "local", "nfs", "ssd", "tmpfs":
	default:
		return "", fmt.Errorf("type must be local|nfs|ssd|tmpfs")
	}
	if !strings.HasPrefix(req.BasePath, "/") || !nixSafeText(req.BasePath, 200) {
		return "", fmt.Errorf("basePath must be an absolute path without control characters")
	}
	if !nixSafeText(req.BackupRepo, 300) {
		return "", fmt.Errorf("backupRepo too long or has control characters")
	}
	entry := "    " + req.Name + " = { type = " + nixString(req.Type) + "; basePath = " + nixString(req.BasePath) + "; backedUp = " + fmt.Sprintf("%t", req.BackedUp) + ";"
	if req.Ephemeral {
		entry += " ephemeral = true;"
	}
	if req.BackedUp && req.BackupRepo != "" {
		entry += " backupRepo = " + nixString(req.BackupRepo) + ";"
	}
	return entry + " };", nil
}

