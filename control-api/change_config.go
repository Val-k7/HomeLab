var reRoleName = regexp.MustCompile(`^(viewer|operator|maintainer|admin)$`)
var reIdentity = regexp.MustCompile(`^[^\s"]{1,120}$`)

// accessRoleChangeHandler is a STRUCTURED editor for config/access.json: it
// grants/revokes a user's role without the operator hand-editing JSON. It reads
// the current file, mutates the users map, and opens a PR with the new content.
func accessRoleChangeHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "POST only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "admin") {
		return
	}
	var req struct {
		User   string `json:"user"`
		Role   string `json:"role"`
		Remove bool   `json:"remove"`
		Reason string `json:"reason"`
	}
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "bad json"})
		return
	}
	if !reIdentity.MatchString(req.User) {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "bad user identity"})
		return
	}
	if !req.Remove && !reRoleName.MatchString(req.Role) {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "role must be viewer|operator|maintainer|admin"})
		return
	}

	// Load current access.json (ordered keys preserved via a generic map is
	// fine here — the file is small and re-serialized deterministically).
	var cfg struct {
		DefaultRole string            `json:"default_role"`
		Users       map[string]string `json:"users"`
		Roles       map[string]int    `json:"roles"`
	}
	if b, err := os.ReadFile(filepath.Join(sourceDir(), "config", "access.json")); err == nil {
		_ = json.Unmarshal(b, &cfg)
	}
	if cfg.Users == nil {
		cfg.Users = map[string]string{}
	}
	if cfg.DefaultRole == "" {
		cfg.DefaultRole = "viewer"
	}
	if cfg.Roles == nil {
		cfg.Roles = map[string]int{"viewer": 0, "operator": 1, "maintainer": 2, "admin": 3}
	}
	if req.Remove {
		delete(cfg.Users, req.User)
	} else {
		cfg.Users[req.User] = req.Role
	}

	content, err := json.MarshalIndent(cfg, "", "  ")
	if err != nil {
		writeJSON(w, http.StatusInternalServerError, map[string]any{"ok": false, "error": err.Error()})
		return
	}
	rec, err := createFileEditPR(r, "access.role", "config/access.json", string(content)+"\n", req.Reason)
	if err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": err.Error(), "change_id": rec.ID})
		return
	}
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "branch": rec.Branch, "pr": prResult{Number: rec.PRNumber, URL: rec.PRURL}})
}

