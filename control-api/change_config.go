// removeStorageClassEntry deletes the `name = { ... };` line from the
// storageClasses block of platform.nix. Pure (unit-tested). Returns the new
// content, or an HTTP status + error when the block/entry cannot be located.
func removeStorageClassEntry(content, name string) (string, int, error) {
	m := reStorageClassesBlock.FindStringSubmatchIndex(content)
	if m == nil {
		return "", http.StatusBadRequest, fmt.Errorf("storageClasses block not found (non-standard file — edit config/platform.nix directly)")
	}
	body := content[m[4]:m[5]]
	lineRe := regexp.MustCompile(`(?m)^[ \t]*` + regexp.QuoteMeta(name) + `[ \t]*=.*;[ \t]*$`)
	loc := lineRe.FindStringIndex(body)
	if loc == nil {
		return "", http.StatusNotFound, fmt.Errorf("storage class not found: %s", name)
	}
	start, end := loc[0], loc[1]
	// Swallow one of the surrounding newlines so no blank line is left behind.
	if end < len(body) && body[end] == '\n' {
		end++
	} else if start > 0 && body[start-1] == '\n' {
		start--
	}
	return content[:m[4]] + body[:start] + body[end:] + content[m[5]:], http.StatusOK, nil
}

// storageClassRemoveHandler deletes one storage class from platform.nix via PR.
// Refuses the platform default class and any class still referenced by an app
// volume — removing those would break path resolution at the next deploy.
func storageClassRemoveHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "POST only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "admin") {
		return
	}
	var req struct {
		Name   string `json:"name"`
		Reason string `json:"reason"`
	}
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil || !reStorageClassName.MatchString(req.Name) {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "bad storage class name"})
		return
	}
	b, err := os.ReadFile(filepath.Join(sourceDir(), "config", "platform.nix"))
	if err != nil {
		writeJSON(w, http.StatusInternalServerError, map[string]any{"ok": false, "error": "cannot read config/platform.nix"})
		return
	}
	content := string(b)
	if regexp.MustCompile(`defaultStorageClass[ \t]*=[ \t]*"` + regexp.QuoteMeta(req.Name) + `"`).MatchString(content) {
		writeJSON(w, http.StatusConflict, map[string]any{"ok": false, "error": "cannot remove the default storage class; change defaultStorageClass first"})
		return
	}
	if apps, _ := filepath.Glob(filepath.Join(sourceDir(), "apps", "*.nix")); apps != nil {
		for _, f := range apps {
			ab, err := os.ReadFile(f)
			if err == nil && strings.Contains(string(ab), `class = "`+req.Name+`"`) {
				writeJSON(w, http.StatusConflict, map[string]any{"ok": false, "error": "storage class in use by " + filepath.Base(f) + "; move its volumes first"})
				return
			}
		}
	}
	next, status, err := removeStorageClassEntry(content, req.Name)
	if err != nil {
		writeJSON(w, status, map[string]any{"ok": false, "error": err.Error()})
		return
	}
	reason := req.Reason
	if reason == "" {
		reason = "remove storage class " + req.Name
	}
	rec, err := createFileEditPR(r, "storage.class", "config/platform.nix", next, reason)
	if err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": err.Error(), "change_id": rec.ID})
		return
	}
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "branch": rec.Branch, "pr": prResult{Number: rec.PRNumber, URL: rec.PRURL}})
}

var reVolName = regexp.MustCompile(`^[a-zA-Z0-9_-]{1,40}$`)

// retargetVolumeClass changes the storage class of one named volume in a v2 app
// module, matching the `name = "VOL"; kind = "..."; class = "..."` shape the
// generator emits. Returns (newContent, oldClass, ok).
func retargetVolumeClass(content, volume, class string) (string, string, bool) {
	re := regexp.MustCompile(`(name = "` + regexp.QuoteMeta(volume) + `"; kind = "[^"]*"; class = ")([^"]*)(")`)
	m := re.FindStringSubmatch(content)
	if m == nil {
		return "", "", false
	}
	old := m[2]
	// Keep the $1/$3 capture-group refs but neutralize any '$' inside the
	// (allowlisted) class so it can't act as a replacement metachar.
	safeClass := strings.ReplaceAll(class, "$", "$$")
	return re.ReplaceAllString(content, `${1}`+safeClass+`${3}`), old, true
}

// appStorageChangeHandler is a STRUCTURED editor: it changes one volume's
// storage class in apps/<app>.nix without the operator hand-editing Nix.
func appStorageChangeHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "POST only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "operator") {
		return
	}
	var req struct {
		App    string `json:"app"`
		Volume string `json:"volume"`
		Class  string `json:"class"`
		Reason string `json:"reason"`
	}
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "bad json"})
		return
	}
	if !reNewAppName.MatchString(req.App) || !reVolName.MatchString(req.Volume) {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "bad app or volume"})
		return
	}
	if _, ok := loadPlatform().StorageClasses[req.Class]; !ok {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "unknown storage class"})
		return
	}
	b, err := os.ReadFile(filepath.Join(sourceDir(), "apps", req.App+".nix"))
	if err != nil {
		writeJSON(w, http.StatusNotFound, map[string]any{"ok": false, "error": "app not found"})
		return
	}
	next, old, ok := retargetVolumeClass(string(b), req.Volume, req.Class)
	if !ok {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "volume not found in app (or non-standard layout)"})
		return
	}
	reason := req.Reason
	if reason == "" {
		reason = fmt.Sprintf("storage: %s/%s %s -> %s", req.App, req.Volume, old, req.Class)
	}
	rec, err := createFileEditPR(r, "app.storage", "apps/"+req.App+".nix", next, reason)
	if err != nil {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": err.Error(), "change_id": rec.ID})
		return
	}
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "branch": rec.Branch, "pr": prResult{Number: rec.PRNumber, URL: rec.PRURL}})
}

// configFileHandler returns the raw source of an editable config/app file so
// the Settings/Storage screens can prefill an editor. Read-only; secrets files
// are never served.
func configFileHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodGet {
		http.Error(w, "GET only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "operator") {
		return
	}
	path := r.URL.Query().Get("path")
	if !relPathOK(path) || strings.HasPrefix(path, "secrets/") {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "path not allowed"})
		return
	}
	b, err := os.ReadFile(filepath.Join(sourceDir(), filepath.FromSlash(path)))
	if err != nil {
		writeJSON(w, http.StatusNotFound, map[string]any{"ok": false, "error": "not found"})
		return
	}
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "path": path, "content": string(b)})
}

// prGitHubStatus fetches one PR's live status. The statusCheckRollup GraphQL
// field needs the Checks read permission, which some fine-grained tokens can't
// even select; when it fails, fall back to state/mergeable/review only, then
// synthesize the CI rollup from `gh run list --commit <head>` — workflow runs
// only need Actions: read. The synthesized entries mimic CheckRun nodes
// (name/conclusion/detailsUrl uppercased) so the UI maps them unchanged.
func prGitHubStatus(env []string, repoArg string, prNumber int) (gh any, warning, ghErr string) {
	res := commandRunner(env, "gh", "pr", "view", fmt.Sprint(prNumber), "--repo", repoArg, "--json", "state,mergeable,reviewDecision,statusCheckRollup")
	rollupWarning := ""
	if res.Err == nil {
		var pr map[string]any
		if err := json.Unmarshal([]byte(res.Output), &pr); err == nil {
			return pr, "", ""
		}
		rollupWarning = "statut CI illisible (sortie gh non parsable); repli sans rollup"
	}
	res = commandRunner(env, "gh", "pr", "view", fmt.Sprint(prNumber), "--repo", repoArg, "--json", "state,mergeable,reviewDecision,headRefOid")
	if res.Err != nil {
		return nil, "", strings.TrimSpace(res.Output)
	}
	var pr map[string]any
	if json.Unmarshal([]byte(res.Output), &pr) != nil {
		return nil, "", "unparseable gh pr view output"
	}
	head, _ := pr["headRefOid"].(string)
	if head != "" {
		runs := commandRunner(env, "gh", "run", "list", "--repo", repoArg, "--commit", head, "--json", "name,status,conclusion,url")
		var rl []struct {
			Name       string `json:"name"`
			Status     string `json:"status"`
			Conclusion string `json:"conclusion"`
			URL        string `json:"url"`
		}
		if runs.Err == nil && json.Unmarshal([]byte(runs.Output), &rl) == nil && len(rl) > 0 {
			rollup := make([]map[string]any, 0, len(rl))
			for _, r := range rl {
				rollup = append(rollup, map[string]any{
					"name":       r.Name,
					"status":     strings.ToUpper(r.Status),
					"conclusion": strings.ToUpper(r.Conclusion),
					"detailsUrl": r.URL,
				})
			}
			pr["statusCheckRollup"] = rollup
			return pr, rollupWarning, ""
		}
	}
	warning = "CI indisponible: le token n'a ni Checks read ni Actions read"
	if rollupWarning != "" {
		warning = rollupWarning
	}
	return pr, warning, ""
}

// changesRefreshHandler enriches local change records with live GitHub PR
// status (CI / mergeable / review) via the gh CLI.
func changesRefreshHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "POST only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "operator") {
		return
	}
	token, err := readGitToken()
	if err != nil {
		writeJSON(w, http.StatusInternalServerError, map[string]any{"ok": false, "error": err.Error()})
		return
	}
	env := gitEnv(changeRepoDir(), token)
	repoArg := ghRepoArg(repoRemoteURL(changeRepoDir()))

	out := []map[string]any{}
	for _, rec := range readChangeRecords(50) {
		entry := map[string]any{
			"id": rec.ID, "type": rec.Type, "title": rec.Title, "status": rec.Status,
			"pr_number": rec.PRNumber, "pr_url": rec.PRURL,
			// Carry the detail-panel fields so refreshing GitHub status does not
			// strip the per-change logs/metadata the UI shows on row expand.
			"time": rec.Time, "actor": rec.Actor, "branch": rec.Branch, "commit": rec.Commit,
			"files": rec.Files, "error": rec.Error, "compare_url": rec.CompareURL,
		}
		if rec.PRNumber > 0 {
			entry["github"], entry["github_warning"], entry["github_error"] = prGitHubStatus(env, repoArg, rec.PRNumber)
		}
		out = append(out, entry)
	}
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "changes": out})
}

// changesRetryHandler retries PR creation for a change whose branch was pushed
// but whose pull request was never opened (token scope failure, transient gh
// error). The branch and commit already exist on the remote, so only the PR
// step is replayed: adopt an existing open PR for the branch if one appeared,
// otherwise create it.
func changesRetryHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "POST only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "operator") {
		return
	}
	var req struct {
		ID string `json:"id"`
	}
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil || req.ID == "" {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "id required"})
		return
	}
	var rec *changeRecord
	for _, c := range readChangeRecords(200) {
		if c.ID == req.ID {
			cc := c
			rec = &cc
			break // records are newest-first; keep the latest entry for this ID
		}
	}
	if rec == nil {
		writeJSON(w, http.StatusNotFound, map[string]any{"ok": false, "error": "change not found"})
		return
	}
	if rec.Branch == "" {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "change has no pushed branch to retry"})
		return
	}
	token, err := readGitToken()
	if err != nil {
		writeJSON(w, http.StatusInternalServerError, map[string]any{"ok": false, "error": err.Error()})
		return
	}
	env := gitEnv(changeRepoDir(), token)
	repoArg := ghRepoArg(repoRemoteURL(changeRepoDir()))

	// A change that already points at a PR is only retryable when that PR was
	// closed without merging — then a fresh PR is opened for the same branch.
	if rec.PRNumber > 0 {
		view := commandRunner(env, "gh", "pr", "view", fmt.Sprint(rec.PRNumber), "--repo", repoArg, "--json", "state,url")
		var pr struct {
			State string `json:"state"`
			URL   string `json:"url"`
		}
		if view.Err == nil && json.Unmarshal([]byte(view.Output), &pr) == nil {
			switch pr.State {
			case "OPEN":
				writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "pr": prResult{Number: rec.PRNumber, URL: rec.PRURL}})
				return
			case "MERGED":
				writeJSON(w, http.StatusConflict, map[string]any{"ok": false, "error": "PR #" + fmt.Sprint(rec.PRNumber) + " was merged; nothing to retry"})
				return
			}
			// CLOSED: fall through and recreate.
		}
	} else if rec.Status != "failed" && rec.Status != "pushed" {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "change is not in a retryable state: " + rec.Status})
		return
	}

	next := *rec
	next.Time = ""
	next.Error = ""
	next.CompareURL = ""
	next.PRNumber = 0
	next.PRURL = ""

	title := rec.Title
	if title == "" {
		title = rec.Branch
	}

	// Rebuild the branch on top of the current main from its recorded files, so
	// the branch always shares history with main and the PR is valid — even when
	// the old branch diverged (e.g. main's history was rewritten, leaving the
	// stale branch with "no history in common with main"). Source the old files
	// from the branch tip first, falling back to the recorded commit.
	if len(rec.Files) == 0 {
		writeJSON(w, http.StatusConflict, map[string]any{"ok": false, "error": "change records no files to replay; re-run the original change"})
		return
	}
	newCommit, err := rebuildChangeBranch(token, rec.Branch, title, rec.Files, rec.Branch, rec.Commit)
	if err != nil {
		writeJSON(w, http.StatusBadGateway, map[string]any{"ok": false, "error": "cannot rebuild branch onto main: " + err.Error()})
		return
	}
	next.Commit = newCommit

	// Adopt an open PR for the branch if one was created out of band.
	list := commandRunner(env, "gh", "pr", "list", "--repo", repoArg, "--head", rec.Branch, "--state", "open", "--json", "number,url")
	if list.Err == nil {
		var prs []prResult
		if json.Unmarshal([]byte(list.Output), &prs) == nil && len(prs) > 0 {
			next.PRNumber = prs[0].Number
			next.PRURL = prs[0].URL
		}
	}
	if next.PRNumber == 0 {
		pr := commandRunner(env, "gh", "pr", "create", "--repo", repoArg, "--base", "main", "--head", rec.Branch, "--title", title, "--body", "Retried PR creation for change "+rec.ID+".")
		if pr.Err != nil {
			next.Status = "pushed"
			next.Error = pr.Output
			next.CompareURL = compareURL(repoArg, rec.Branch)
			appendChangeRecord(r, next)
			writeJSON(w, http.StatusBadGateway, map[string]any{"ok": false, "error": strings.TrimSpace(pr.Output), "change_id": next.ID, "compare_url": next.CompareURL})
			return
		}
		next.PRURL = strings.TrimSpace(pr.Output)
		if m := rePRNumber.FindStringSubmatch(next.PRURL); len(m) == 2 {
			next.PRNumber, _ = strconv.Atoi(m[1])
		}
	}
	next.Status = "open"
	appendChangeRecord(r, next)
	appendAuditEvent(r, auditEvent{Op: rec.Type + ".retry", Kind: "change", Target: rec.Branch, Risk: "medium", Result: "created", Status: http.StatusOK, Commit: next.Commit, Message: next.PRURL})
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": next.ID, "branch": next.Branch, "pr": prResult{Number: next.PRNumber, URL: next.PRURL}})
}

// changesMergeHandler merges the open PR of a change. Merging lands the change
// on main, which triggers a deploy, so it needs the maintainer role. The merge
// is gated on GitHub's own mergeability (CI green + no blocking review): gh
// refuses a not-yet-mergeable PR and we surface that error.
func changesMergeHandler(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "POST only", http.StatusMethodNotAllowed)
		return
	}
	if !requireRole(w, r, "maintainer") {
		return
	}
	var req struct {
		ID string `json:"id"`
	}
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil || req.ID == "" {
		writeJSON(w, http.StatusBadRequest, map[string]any{"ok": false, "error": "id required"})
		return
	}
	var rec *changeRecord
	for _, c := range readChangeRecords(200) {
		if c.ID == req.ID {
			cc := c
			rec = &cc
			break
		}
	}
	if rec == nil || rec.PRNumber == 0 {
		writeJSON(w, http.StatusNotFound, map[string]any{"ok": false, "error": "no open PR for this change"})
		return
	}
	token, err := readGitToken()
	if err != nil {
		writeJSON(w, http.StatusInternalServerError, map[string]any{"ok": false, "error": err.Error()})
		return
	}
	env := gitEnv(changeRepoDir(), token)
	repoArg := ghRepoArg(repoRemoteURL(changeRepoDir()))
	// --squash keeps main history linear; --delete-branch cleans up the change
	// branch once it has landed.
	res := commandRunner(env, "gh", "pr", "merge", fmt.Sprint(rec.PRNumber), "--repo", repoArg, "--squash", "--delete-branch")
	if res.Err != nil {
		writeJSON(w, http.StatusBadGateway, map[string]any{"ok": false, "error": strings.TrimSpace(res.Output)})
		return
	}
	merged := *rec
	merged.Time = ""
	merged.Status = "merged"
	merged.Error = ""
	appendChangeRecord(r, merged)
	appendAuditEvent(r, auditEvent{Op: rec.Type + ".merge", Kind: "change", Target: rec.Branch, Risk: "high", Result: "merged", Status: http.StatusOK, Commit: rec.Commit, Message: rec.PRURL})
	writeJSON(w, http.StatusOK, map[string]any{"ok": true, "change_id": rec.ID, "pr_number": rec.PRNumber})
}

